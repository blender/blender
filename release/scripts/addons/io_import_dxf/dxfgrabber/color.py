class TrueColor(int):
    def rgb(self):
        return (self >> 16) & 0xFF, (self >> 8) & 0xFF, self & 0xFF

    @property
    def r(self):
        return (self >> 16) & 0xFF

    @property
    def g(self):
        return (self >> 8) & 0xFF

    @property
    def b(self):
        return self & 0xFF

    def __getitem__(self, item):
        if item == 0:
            return self.r
        elif item == 1:
            return self.g
        elif item == 2:
            return self.b
        raise IndexError(item)

    @staticmethod
    def from_rgb(r, g, b):
        return TrueColor(((r & 0xff) << 16) | ((g & 0xff) << 8) | (b & 0xff))

    @staticmethod
    def from_aci(index):
        if index < 1:
            raise IndexError(index)
        return dxf_default_colors[index]


def aci_to_true_color(index):
    return TrueColor.from_aci(index)


